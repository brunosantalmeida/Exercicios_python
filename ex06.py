# calculo do dobro, do triplo e da raiz quadrada de um numero 

n = int(input('Digite um numero: '))
d = n * 2
t = n * 3
r = n ** (1/2)
print('O doblo de {} vale {}.'.format(n, d))
print('O triplo de {} vale {}\n.A raiz quadrada de  {} e igual a {:.2f}.'.format(n, t, n, r))

